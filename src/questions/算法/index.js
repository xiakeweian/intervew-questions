function twoSum1(numbers, target) {
  let mark = []
  let isUniq = false
  numbers.map((item, index, self) => {
    if (
      self.includes(target - item) &&
      self.includes(item) &&
      self.indexOf(target - item) !== index
    ) {
      if (target - item !== item) {
        isUniq = true
      } else {
        isUniq = false
      }
      mark.push(index + 1, self.indexOf(target - item) + 1)
    }
  })
  if (isUniq) {
    mark = [...new Set(mark)]
  }
  return mark.sort((a, b) => a - b)
}

function twoSum(numbers, target) {
  let obj = []
  for (let i = 0; i < numbers.length; i++) {
    for (let j = 0; j < numbers.length; j++) {
      if (target === numbers[i] + numbers[j] && i !== j) {
        obj = [i + 1, j + 1]
      }
    }
  }
  return obj.sort((a, b) => a - b)
}

